
package object.oriented.java;


public class MathDemo {

    public static void main(String[] args) {
        
        System.out.println("Abs : "+Math.abs(-1));
        System.out.println("Sqrt : "+Math.sqrt(25));
        System.out.println("Pow : "+Math.pow(2, 3));
        System.out.println("PI : "+Math.PI);
        System.out.println("Log : "+Math.log(2.0));
        System.out.println("Exp : "+Math.exp(1.0));
        System.out.println("Max : "+Math.max(-1, -2.5));
        System.out.println("Min : "+Math.min(-1, -2.5));
        System.out.println("Ceiling : "+Math.ceil(-8.6));
        System.out.println("Floor : "+Math.floor(-8.6));
        
    }
    
}
